    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      String sd = dsid.getText();
Connection conn = null;
PreparedStatement ptst = null;
PreparedStatement removeTurmaStmt = null;
PreparedStatement buscaNomeStmt = null;
        ResultSetImpl nomeRs = null;

try {
    conn = (Connection) Conexao.obterConexao();

    
    String buscaNomeSql = "SELECT nome FROM estucad WHERE ID = ?";
    buscaNomeStmt = (PreparedStatement) conn.prepareStatement(buscaNomeSql);
    buscaNomeStmt.setString(1, sd);
    nomeRs = (ResultSetImpl) buscaNomeStmt.executeQuery();

    String estudanteNome = null;
    if (nomeRs.next()) {
        estudanteNome = nomeRs.getString("nome");
    }

    if (estudanteNome == null || estudanteNome.trim().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Estudante não encontrado.");
        return;
    }

    String sql = "DELETE FROM estucad WHERE ID = ?";
    ptst = (PreparedStatement) conn.prepareStatement(sql);
    ptst.setString(1, sd);
    ptst.executeUpdate();
    JOptionPane.showMessageDialog(null, "Aluno deletado");

    
    String removeAlunoSql = "UPDATE turma SET " +
                            "TurmaADS1 = REPLACE(TurmaADS1, ?, ''), " +
                            "TurmaADS2 = REPLACE(TurmaADS2, ?, ''), " +
                            "TurmaCC1 = REPLACE(TurmaCC1, ?, ''), " +
                            "TurmaCC2 = REPLACE(TurmaCC2, ?, ''), " +
                            "TurmaTeatro1 = REPLACE(TurmaTeatro1, ?, ''), " +
                            "TurmaTeatro2 = REPLACE(TurmaTeatro2, ?, ''), " +
                            "SemTurma = REPLACE(SemTurma, ?, '')";
    
    removeTurmaStmt = (PreparedStatement) conn.prepareStatement(removeAlunoSql);
    String alunoInfo = sd + " - " + estudanteNome + ", "; 
    for (int i = 1; i <= 7; i++) {
        removeTurmaStmt.setString(i, alunoInfo);
    }
    removeTurmaStmt.executeUpdate();
    JOptionPane.showMessageDialog(null, "Aluno removido das turmas");

    dsid.setText("");
} catch (Exception e) {
    JOptionPane.showMessageDialog(null, e);
} finally {
    try {
        if (nomeRs != null) nomeRs.close();
        if (buscaNomeStmt != null) buscaNomeStmt.close();
        if (ptst != null) ptst.close();
        if (removeTurmaStmt != null) removeTurmaStmt.close();
        if (conn != null) Conexao.fecharConexao(conn);
    } catch (Exception e) {
        e.printStackTrace();
    }
}

dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

