    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       String id = estura.getText();
    try {
        Connection conn = (Connection) Conexao.obterConexao();
        String sql = "SELECT * FROM estucad WHERE ID=" + id;
        PreparedStatement ptst = (PreparedStatement) conn.prepareStatement(sql);
        ResultSet rs = ptst.executeQuery();
        estusenha.setEditable(false);

        if (rs.next()) {
            
            String nome = rs.getString("nome");
            String nomemae = rs.getString("nomemae");
            String nascimento = rs.getString("nascimento");
            String tel = rs.getString("tel");
            String cpf = rs.getString("cpf");
            String endereco = rs.getString("endereco");
            String bairro = rs.getString("bairro");
            String numero = rs.getString("numero");
            String complemento = rs.getString("complemento");
            String senha = rs.getString("senha");
            String email = rs.getString("email");
            String palavra = rs.getString("palavra");     
          
            estunome.setText(nome);
            estunomemae.setText(nomemae);
            estunascimento.setText(nascimento);
            estutelefone.setText(tel);
            estucpf.setText(cpf);
            estuend.setText(endereco);
            estubairro.setText(bairro);
            estunum.setText(numero);
            estucomp.setText(complemento);
            estusenha.setText(senha);
            estuemail.setText(email); 
            estupalavra.setText(palavra); 
          if ("0".equals(numero)) {
                semnumero.setSelected(true);
                estunum.setEditable(false);
                estunum.setBackground(Color.DARK_GRAY);
                estunum.setForeground(Color.WHITE);
            } else {
                semnumero.setSelected(false);
                estunum.setEditable(true);
                estunum.setBackground(Color.WHITE);
                estunum.setForeground(Color.BLACK);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nenhum registro encontrado para o RA fornecido");
        }

        Conexao.fecharConexao(conn);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

