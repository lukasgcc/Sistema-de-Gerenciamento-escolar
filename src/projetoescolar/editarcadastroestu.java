    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         String id = estura.getText();
        String email = estuemail.getText();
        String tel = estutelefone.getText();
        String endereco = estuend.getText();
        String numero = estunum.getText();
        String bairro = estubairro.getText();
        String complemento = estucomp.getText();
        String senha = estusenha.getText();
        String palavra = estupalavra.getText();

        try {
            Connection conn = (Connection) Conexao.obterConexao();
            String sql = "UPDATE estucad SET email=?, tel=?, endereco=?, numero=?, bairro=?, complemento=?, senha=?, palavra=? WHERE id=?";
            PreparedStatement ptst = (PreparedStatement) conn.prepareStatement(sql);
            ptst.setString(1, email);
            ptst.setString(2, tel);
            ptst.setString(3, endereco);
            ptst.setString(4, numero);
            ptst.setString(5, bairro);
            ptst.setString(6, complemento);
            ptst.setString(7, senha);
            ptst.setString(8, palavra);
            ptst.setString(9, id);
            
            ptst.execute();
            JOptionPane.showMessageDialog(null, "Cadastro alterado com sucesso");
            Conexao.fecharConexao(conn);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar cadastro");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

