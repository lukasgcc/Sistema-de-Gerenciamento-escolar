    private void preencherCampos(String ra) {
      try {
   
    String raAluno = SessaoAluno.getInstancia().getRaAluno();

    
    Connection conn = (Connection) Conexao.obterConexao();

    
    String sql = "SELECT * FROM estucad WHERE id=?";
    PreparedStatement pst = (PreparedStatement) conn.prepareStatement(sql);
    pst.setString(1, raAluno);

    
    ResultSetImpl rs = (ResultSetImpl) pst.executeQuery();

    
    if (rs.next()) {
        estura.setText(rs.getString("id"));
        estunome.setText(rs.getString("nome"));
        estunomemae.setText(rs.getString("nomemae"));
        estunascimento.setText(rs.getString("nascimento"));
        estuemail.setText(rs.getString("email"));
        estutelefone.setText(rs.getString("tel"));
        estucpf.setText(rs.getString("cpf"));
        estuend.setText(rs.getString("endereco"));
        estunum.setText(rs.getString("numero"));
        estubairro.setText(rs.getString("bairro"));
        estucomp.setText(rs.getString("complemento"));
        estusenha.setText(rs.getString("senha"));
        estupalavra.setText(rs.getString("palavra"));
    } else {
        JOptionPane.showMessageDialog(null, "Aluno não encontrado no banco de dados");
    }

 
    rs.close();
    pst.close();
    conn.close();
    
    estura.setBackground(Color.LIGHT_GRAY);
            estunome.setBackground(Color.LIGHT_GRAY);
             estunomemae.setBackground(Color.LIGHT_GRAY);
            estucpf.setBackground(Color.LIGHT_GRAY);
            estunascimento.setBackground(Color.LIGHT_GRAY);

} catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao carregar dados: " + e.getMessage());
        }
    }

