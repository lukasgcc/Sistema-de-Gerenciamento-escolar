    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         String raAluno = estura.getText().trim(); // ObtÃ©m o RA do aluno
    
    
    if (raAluno.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, insira o RA do aluno.");
        return;
    }
    
    Connection conn = null;
    PreparedStatement stmt = null;

    try {
        conn = (Connection) Conexao.obterConexao();

        
        String deletarExtensaoSql = "DELETE FROM extensao WHERE id = ?";
        stmt = (PreparedStatement) conn.prepareStatement(deletarExtensaoSql);
        stmt.setString(1, raAluno);
        
        int linhasAfetadas = stmt.executeUpdate();
        
        if (linhasAfetadas > 0) {
            JOptionPane.showMessageDialog(this, "Registros deletados com sucesso.");
        } else {
            JOptionPane.showMessageDialog(this, "Nenhum registro encontrado para deletar.");
        }
        
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Erro ao deletar registros: " + e.getMessage());
    } finally {
        try {
            if (stmt != null) stmt.close();
            if (conn != null) conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

